#Program menentukan segitiga sama sisi

S1 = int(input("Masukan panjang sisi 1: "))
S2 = int(input("Masukan panjang sisi 2: "))
S3 = int(input("Masukan panjang sisi 3: "))

if S1==S2==S3:
    print("Bangun datar ini adalah Segitiga Sama Sisi")
else:
    print("Bangun datar ini bukanlah Segitiga Sama Sisi")